import java.util.*;
public class nnnsum {
    public static void main(String args[]){
        int sum=0;
        Scanner sc=new Scanner(System.in);
        int n1=sc.nextInt();
        int n2=sc.nextInt();
        for(int i=n1;i<=n2;i++){
            sum+=i;
        }
        System.out.println(sum);
    }
}
